package CACC_2019.week6_Loop;
/*Angelina Chen: This is the "for" loop in the assignment.*/
public class CACCAngelinaWeek6second {
        public static void main(String[] args) {
            int num = 50, sum = 0;
            for(int i = 0; i <= num; i += 1)
            {
                sum += i ;
                System.out.println("Sum = " + sum);
            }
            System.out.println("Sum = " + sum);
        }
    }


